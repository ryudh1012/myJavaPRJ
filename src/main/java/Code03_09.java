public class Code03_09 {
    public static void main(String[] args){
        int num = 20;

        System.out.println(++num + " ");

        System.out.println(num++ + " ");
        System.out.println(num + " ");

        System.out.print(--num + " ");

        System.out.println(num-- + " ");

        num +=3;
        System.out.println(num + " ");

        num -=3;
        System.out.println(num + " ");

        num *=3;
        System.out.println(num + " ");

        num /=3;
        System.out.println(num + " ");

        num %=3;
        System.out.println(num + " ");
    }
}
